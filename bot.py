def translate_word(word):
    w = word.lower().strip()
    if w in words:
        return words[w]
    return (w, w)

def translate_text(text):
    words_list = text.split()
    result = []
    transcript = []
    missing = []
    
    for w in words_list:
        el, tr = translate_word(w)
        result.append(el)
        transcript.append(tr)
        if el == w:
            missing.append(w)
    
    output = " ".join(result) + "\n\n" + " ".join(transcript)
    
    if missing:
        output += f"\n\n❌ Нет в словаре: {', '.join(missing)}"
    
    return output

def reverse_translate(text):
    words_list = text.lower().split()
    reverse_words = {v[0]: k for k, v in words.items()}
    result = []
    missing = []
    
    for w in words_list:
        if w in reverse_words:
            result.append(reverse_words[w])
        else:
            result.append(w)
            missing.append(w)
    
    output = " ".join(result)
    
    if missing:
        output += f"\n\n❌ Нет в словаре: {', '.join(missing)}"
    
    return output

